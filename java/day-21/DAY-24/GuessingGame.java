import java.util.Random;
import java.util.Scanner;
public class GuessingGame {

  public static void main(String[] args) {

		Random rand = new Random();
		int numberToGuess = rand.nextInt(100);
                int numberOfTries =0;
                Scanner input = new Scanner(System.in);
                int  guess;
                boolean win = false;
while(win==false){
	System.out.println("Guess a no between 1 and 100;");
        guess = input.nextInt();
        numberOfTries++;

	if(guess==numberToGuess){
		win=true;
	}
	else if(guess<numberToGuess){
	System.out.println("your Guess is low");
	}
	else if(guess>numberToGuess){
	System.out.println("your Guess is high");
	}
        }
	System.out.println("you win thanx for playing");
	System.out.println("The no was\t" +numberToGuess);
	System.out.println("it took you\t"+numberOfTries + "\ttries");
	
	if(numberOfTries==1)
	System.out.println("wow your guess is perfect");
	
	else if(numberOfTries<=5)
	System.out.println("your guess is good");
        
	else if(numberOfTries>=5)
	System.out.println(" your guess is bad keep working on it");
	
       }
    }
